// Addition
console.log("15 + 3 ergibt:", 15 + 3);

// Subtraktion
console.log("15 - 3 ergibt:", 15 - 3);

// Multiplikation
console.log("15 * 3 ergibt:", 15 * 3);

// Division 
console.log("15 / 3 ergibt:", 15 / 3);

// Modulo 
console.log("15 % 3 ergibt", 15 % 3);

// Potenzahlen
console.log("15 hoch 3 ergibt (15 x 15 x 15):", 15 ** 3);

// Zahlen runden
console.log("322,34 ist abgerundet:", Math.round(322.34));

// Größte Zahl rauspicken
console.log("Die größte Zahl von: 14, 252, 467 ist:", Math.max(14, 252, 467));

// Kleinste Zahl rauspicken
console.log("Die kleinste Zahl von: 14, 252, 467 ist:", Math.min(14, 252, 467));